public class ke2 {
    String nama, a, w;
    Integer n, p;

    public pk2(String nama, String a, String w, String n, String p){
        this.nama = nama;
        this.a = a;
        this.w = w;
        this.n = n;
        this.p = p;
    }

    public String getnama() {
        return nama;
    }

    public String geta() {
        return a;
    }

    public String getw() {
        return w;
    }

    public String getn() {
        return n;
    }

    public String getp() {
        return p;
    }
}